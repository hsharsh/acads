function d = fexp2(x,y)
%{
   d = [
       y(2)                     -0.1*y(2)-x;
       -0.1*y(2)-x              0.01*y(2)+0.1*x-1;
       0.01*y(2)+0.1*x-1        -0.001*y(2)-0.01*x+0.1;
       -0.001*y(2)-0.01*x+0.1   0.0001*y(2)+0.001*x-0.01;
       ];
%}
   d = [
       x^2-4*y;
       2*x-4*x^2+16*y;
       2-8*x+16*x^2-64*y;
       -8+32*x-64*x^2+256*y; 
       ];
end